    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String id = txtID.getText();
        String name = txtName.getText();
        String lastName = txtLastName.getText();
        String phone = txtPhone.getText();

        // Crear un objeto Resident con los datos obtenidos
        Resident newResident = new Resident(id, name, lastName, phone);

        // Llamar al mÃ©todo para aÃ±adir el residente a MongoDB
        residentDAO.createResident(newResident);

        // ConfirmaciÃ³n de que se ha aÃ±adido el residente
        JOptionPane.showMessageDialog(this, "Residente aÃ±adido exitosamente");
// Limpiar los campos despuÃ©s de aÃ±adir
    clearFields();
}

// MÃ©todo para limpiar los campos de texto
private void clearFields() {
    txtID.setText("");
    txtName.setText("");
    txtLastName.setText("");
    txtPhone.setText("");
        
    boolean isIDValid = condoManagementController.validateID(txtID);
    boolean isNameValid = condoManagementController.validateName(txtName);
    boolean isLastNameValid = condoManagementController.validateLastName(txtLastName);
    boolean isPhoneValid = condoManagementController.validatePhone(txtPhone);


    if (txtName.getText().trim().isEmpty() || txtID.getText().trim().isEmpty() || txtLastName.getText().trim().isEmpty() || txtPhone.getText().trim().isEmpty()) {
  
    JOptionPane.showMessageDialog(btnAdd, "Por favor complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!isIDValid) {
   
    JOptionPane.showMessageDialog(btnAdd, "El campo ID debe contener solo numeros.", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!isNameValid) {
    
    JOptionPane.showMessageDialog(btnAdd, "El nombre debe tener solo letras.", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!isLastNameValid) {
    
    JOptionPane.showMessageDialog(btnAdd, "El apellido debe contener solo letras.", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!isPhoneValid) {
   
    JOptionPane.showMessageDialog(btnAdd, "El telefono debe constar solo de numeros.", "Error", JOptionPane.ERROR_MESSAGE);
} else {
    
    JOptionPane.showMessageDialog(btnAdd, "Sus datos han sido guardados.", "Confirmacion", JOptionPane.INFORMATION_MESSAGE);
}


    }//GEN-LAST:event_btnAddActionPerformed

