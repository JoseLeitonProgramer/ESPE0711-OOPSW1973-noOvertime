    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String id = txtID.getText();
        String name = txtName.getText();
        String lastName = txtLastName.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();


        Resident newResident = new Resident(id, name, lastName, phone, email);


        
    boolean isIDValid = validationController.validateID(txtID);
    boolean isNameValid = validationController.validateName(txtName);
    boolean isLastNameValid = validationController.validateLastName(txtLastName);
    boolean isPhoneValid = validationController.validatePhone(txtPhone);

    if (txtName.getText().trim().isEmpty() || txtID.getText().trim().isEmpty() || 
    txtLastName.getText().trim().isEmpty() || txtPhone.getText().trim().isEmpty() || 
    txtEmail.getText().trim().isEmpty()) {
    
    JOptionPane.showMessageDialog(btnAdd, "Por favor complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
    return; // Detiene la ejecuciÃ³n si hay campos vacÃ­os
}

    if (!isIDValid) {
    JOptionPane.showMessageDialog(btnAdd, "El campo ID debe contener solo numeros.", "Error", JOptionPane.ERROR_MESSAGE);
    return; // Detiene la ejecuciÃ³n si el ID no es vÃ¡lido
}

    if (!isNameValid) {
    JOptionPane.showMessageDialog(btnAdd, "El nombre debe tener solo letras.", "Error", JOptionPane.ERROR_MESSAGE);
    return; // Detiene la ejecuciÃ³n si el nombre no es vÃ¡lido
}

    if (!isLastNameValid) {
    JOptionPane.showMessageDialog(btnAdd, "El apellido debe contener solo letras.", "Error", JOptionPane.ERROR_MESSAGE);
    return; // Detiene la ejecuciÃ³n si el apellido no es vÃ¡lido
}

    if (!isPhoneValid) {
    JOptionPane.showMessageDialog(btnAdd, "El telefono debe constar solo de numeros.", "Error", JOptionPane.ERROR_MESSAGE);
    return; // Detiene la ejecuciÃ³n si el telÃ©fono no es vÃ¡lido
}

    // Llamar al mÃ©todo para aÃ±adir el residente a MongoDB
    residentController.createResident(newResident);
        
    // Si todas las validaciones pasan, los datos se guardan
    JOptionPane.showMessageDialog(btnAdd, "Sus datos han sido guardados.", "Confirmacion", JOptionPane.INFORMATION_MESSAGE);


    
    }//GEN-LAST:event_btnAddActionPerformed

